export type GearTier = "flawless" | "exquisite" | "fine" | "superior"
// People use mostly the color than the names above
export type GearColorTier = "golden" | "purple" | "blue" | "green" 

export const tierMap = {
  golden: "flawless",
  purple: "exquisite",
  blue: "fine",
  green: "superior",
}

export type GearType = "weapon" | "armor" | "helmet" | "accessory"

export type GearCapability = "nothing" | "troop attack" | "tank" | "physical commander" | "focus commander"

export type Gear = {
  name: string
  img: string
  tier: GearTier
  type: GearType
  // TODO maybe I just need `bestFor` field, as "nothing" tells me it's spendable
  // but also, maybe `spendable` becomes a score. I'll know better when I
  // research the whole available set of equipment
  spendable: boolean
  bestFor: GearCapability
  cDamage: string
  cHp: number
  cCommand: number
  cAttack: number
  cDefense: number
  cFocus: number
  cInitiative: number
  uAttack: number
  uDefense: number
  // Only a single gear has `uSiege`, so I make it optional to avoid putting it
  // in every gear
  uSiege?: number,
}

